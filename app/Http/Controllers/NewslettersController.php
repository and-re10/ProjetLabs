     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(NewslettersRequest $request)
    {
        $newsletters = Newsletter::all();
        $newsletter = new Newsletter();

        for ($i = 0; $i < (count($newsletters)); $i++){
            if (request('email') === $newsletters[$i]->email){
                return redirect()->back()->with('error', 'Email existant');
            };
        };

        $newsletter->email = request('email');
        $newsletter->save();

        return redirect()->back()->with('messages', 'Email envoy√© avec success');
    }

